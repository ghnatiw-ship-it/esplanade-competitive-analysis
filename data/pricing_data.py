import pandas as pd

_TIER_SORT = {"Local": 1, "City-wide": 2, "Global": 3}

def _make_pricing_df(rows):
    df = pd.DataFrame(rows, columns=["Name", "Total (CAD)", "Region", "Breakdown"])
    df["_is_incomplete"] = df["Breakdown"].astype(str).str.startswith("*")
    extracted = df["Total (CAD)"].str.replace(r"[^\d.]", "", regex=True).replace("", None)
    df["_price_value"] = pd.to_numeric(extracted, errors="coerce")
    df["_sort_bucket"] = 0
    df.loc[df["_is_incomplete"], "_sort_bucket"] = 1
    df.loc[df["_price_value"].isna(), "_sort_bucket"] = 2
    df["_price_sort"] = df["_price_value"].fillna(9999)
    df["_tier_sort"] = df["Region"].map(_TIER_SORT).fillna(2)
    df.loc[df["_is_incomplete"] & df["Total (CAD)"].ne("N/A"), "Total (CAD)"] = df["Total (CAD)"] + "*"
    df = df.sort_values(["_sort_bucket", "_price_sort", "_tier_sort", "Name"]).drop(
        columns=["_sort_bucket", "_price_sort", "_tier_sort"]
    ).reset_index(drop=True)
    return df

